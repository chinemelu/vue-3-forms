<template>
   <input
    type="checkbox"
    v-bind="$attrs"
    @change="handleChange"
    :checked="modelValue"
    :id="uuid"
  />
  <label :for="uuid" v-if="label">{{label}}</label>
</template>

<script>
import UniqueID from '../features/UniqueID.js'
export default {
  props: {
    label: {
      type: String,
      default: ''
    },
    modelValue: {
      type: Boolean,
      default: false
    }
  },
  setup (props, { emit }) {
    const handleChange = (event) => {
      emit('update:modelValue', event.target.checked)
    }

    const uuid = UniqueID().getID()
    return { handleChange, uuid }
  }
}
</script>

